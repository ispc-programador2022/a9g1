#Retorna el producto de 2 parámetros.



val1=int(input("Por favor, ingrese el primer número que desea multiplicar: "))
val2=int(input("Por favor, ingrese el segundo número por el cual desea multiplicar: "))



def producto (x, y):
    return x * y


print("El resultado de su operación es:: ",producto(val1, val2))