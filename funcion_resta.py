#Funsión Resta
#Retorna la resta de 2 parámetros.



val1=int(input("Por favor, ingrese el primer número que desea restar: "))
val2=int(input("Por favor, ingrese el segundo número que desea restar: "))



def resta (x, y):
    return x - y


print("La resta de sus números da el valor de: ",resta(val1, val2))